# repetition 
num1=5
print(f"\nThe value of num1 is : {num1}\n")

repeated_string = ""
for i in range(num1):
    repeated_string += str1 + "\n"
print("Repeating the string using for loop:\n",repeated_string)

# finding length of a string
length = len(repeated_string)
print(f"\nLength of the repeated string is: {length}")